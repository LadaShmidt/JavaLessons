public class OperationsMethods {
    static double sum (byte a, double b) {
        return a + b;
    }
    static double substraction (int a, float b) {
        return a - b;
    }
    static long multiplication (long a, short b) {
        return a * b;
    }
    static double devision (float a, double b) {
        return a / b;
    }
    static int reminder (int a, byte b) {
        return a % b;
    }
}
